crypto_core::PublicKey import_rsa_verify_key()
{
	auto key = crypto_core::PublicKey::import_der(crypto_core::AsymmetricKeyAlgorithm::rsa, rsa_pss_public_key_der, crypto_core::KeyUsage::verify);
	require(key.has_value());
	return key.value();
}

void test_native_provider_verifies_rsa_pss_sha256_signature()
{
	crypto_core::NativeProvider provider;
	auto public_key = import_rsa_verify_key();
	const auto message = bytes("rsa pss message");

	auto result = crypto_core::verify(provider, {crypto_core::SignatureAlgorithm::rsa_pss_sha256, &public_key, rsa_pss_signature}, message.bytes());
	require(result.has_value());
	require(result.value().is_valid());
}

void test_native_provider_rejects_tampered_rsa_pss_inputs()
{
	crypto_core::NativeProvider provider;
	auto public_key = import_rsa_verify_key();
	const auto message = bytes("rsa pss message");
	const auto tampered_message = bytes("rsa pss tampered");

	auto bad_message = crypto_core::verify(provider, {crypto_core::SignatureAlgorithm::rsa_pss_sha256, &public_key, rsa_pss_signature}, tampered_message.bytes());
	require(bad_message.has_value());
	require(!bad_message.value().is_valid());

	auto tampered_signature = std::vector<std::uint8_t>(rsa_pss_signature.begin(), rsa_pss_signature.end());
	tampered_signature[0] ^= 0x80U;
	auto bad_signature = crypto_core::verify(provider, {crypto_core::SignatureAlgorithm::rsa_pss_sha256, &public_key, tampered_signature}, message.bytes());
	require(bad_signature.has_value());
	require(!bad_signature.value().is_valid());
}

